/**
 * Get current language from URL or localStorage
 */
function getCurrentLang() {
  if (window.location.pathname.startsWith('/en/') || window.location.pathname === '/en') {
    return 'en';
  }
  const stored = localStorage.getItem('eurygo_lang');
  if (stored && (stored === 'es' || stored === 'en')) {
    return stored;
  }
  return 'es';
}

/**
 * Translate a key
 */
function t(key) {
  const lang = getCurrentLang();
  return translations[lang][key] || translations['es'][key] || key;
}

/**
 * Apply translations to all elements with data-i18n attribute
 */
function applyTranslations() {
  const lang = getCurrentLang();
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    const val = translations[lang][key];
    if (val) {
      if (el.tagName === 'INPUT' && el.type !== 'checkbox') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });

  // Update lang switch active state
  document.querySelectorAll('.lang-switch__btn').forEach(function(btn) {
    btn.classList.toggle('lang-switch__btn--active', btn.dataset.lang === lang);
  });
}

/**
 * Switch language
 */
function switchLang(lang) {
  localStorage.setItem('eurygo_lang', lang);

  // Track language switch event
  if (typeof gtag === 'function') {
    gtag('event', 'language_switch', {
      'from_language': getCurrentLang(),
      'to_language': lang
    });
  }

  // Check for explicit alternate language URL (course detail pages, etc.)
  var altUrl = document.querySelector('[data-alt-lang-url]');
  if (altUrl) {
    var targetUrl = altUrl.getAttribute('data-alt-lang-url');
    var currentLang = window.location.pathname.startsWith('/en') ? 'en' : 'es';
    if (lang !== currentLang && targetUrl) {
      window.location.href = targetUrl;
      return;
    }
  }

  // Default redirect: prepend/remove /en/ prefix
  var currentPath = window.location.pathname;
  if (lang === 'en') {
    if (!currentPath.startsWith('/en')) {
      window.location.href = '/en/' + currentPath.replace(/^\//, '');
    }
  } else {
    if (currentPath.startsWith('/en')) {
      window.location.href = currentPath.replace(/^\/en\/?/, '/');
    }
  }
}

// Init on DOM ready
document.addEventListener('DOMContentLoaded', function() {
  applyTranslations();

  document.querySelectorAll('.lang-switch__btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      switchLang(this.dataset.lang);
    });
  });
});
